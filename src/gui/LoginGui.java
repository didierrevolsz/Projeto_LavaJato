        Point p = this.getLocation();
        LoginGui loginGui = this;
           
        new Thread(){
            
            @Override
            public void run() { 
           try {
                for (int i = 0; i < 6; i++) {
                 loginGui.setLocation(p.x-10,p.y);
                   
                 sleep(20);
                 
                 loginGui.setLocation(p.x+10,p.y);
                 
                 sleep(20);
                 
                    }  
                 loginGui.setLocation(p.x,p.y); 
                
                } catch(InterruptedException ex){
                   Logger.getLogger(LoginGui.class.getName()).log(Level.SEVERE, null, ex);
                   }
            }
           
        }.start();
